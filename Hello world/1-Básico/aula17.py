# if  / elif      / else
# se  / se não se / se não

condicao1 = False
condicao2 = True
condicao3 = False
condicao4 = False

if condicao1:
    print('if da condicao 1')
elif condicao2:
    print('if da condicao 2')
elif condicao3:
    print('if da condicao 3')
elif condicao4:
    print('if da condicao 4')
else:
    print('nunhuma condição foi satisfeita')